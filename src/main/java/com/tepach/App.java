    private void JB_cargartxtActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JB_cargartxtActionPerformed
        ruta = archivo.leer(this);
        jtxt_ruta.setText(ruta);
        jtxt_ruta.setEditable(false);
        num_archivo = archivo.numLineas(ruta);
        jtf_cantidaddetxt.setText(String.valueOf(num_archivo));
        String concat = jLbl_numlineas.getText().concat(" de " + archivo.nomTxt(ruta));
        jLbl_numlineas.setText(concat);
        jtf_dvidiren.setEditable(true);

        jb_partir.setEnabled(true);
    }//GEN-LAST:event_JB_cargartxtActionPerformed

    private void jb_partirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jb_partirActionPerformed
        // TODO add your handling code here:
        if (!jtf_dvidiren.getText().isEmpty()) {
            cantidad = Integer.parseInt(jtf_dvidiren.getText());
            if (!(cantidad > num_archivo)) {
                int division = (int) (num_archivo / cantidad);
                    if (num_archivo%cantidad != 0) {
                    division=division+1;
                }
                JOptionPane.showMessageDialog(rootPane, "Se crearan " + division + " archivos en la ruta: \n" + archivo.carpetaTxt(ruta), "Informaci√≥n", JOptionPane.INFORMATION_MESSAGE);
                String guarda_en=archivo.carpetaTxt(ruta);
                dividir.particiona(ruta, guarda_en, cantidad);
                
                           }
        }else{
            JOptionPane.showMessageDialog(rootPane, "Ingresa la cantidad de lineas por archivo");
        }

    }//GEN-LAST:event_jb_partirActionPerformed

